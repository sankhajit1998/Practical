// Simple if else statement.

package sankha_java;

public class if_else {

	public static void main(String[] args) {    // main method
		
		int a = 15;   // declare a = 5
		int b = 3;   // declare b = 3
		if(a + b < 10) {    // condition
			System.out.println("a + b is less than 10.");   // print the output
		}
		else {
			System.out.println("a + b is greater than 10.");   // else condition output
		}

	}

}